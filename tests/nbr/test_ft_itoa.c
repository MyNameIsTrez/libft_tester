#include "libft_tests.h"

////////////////////////////////////////////////////////////////////////////////

Test(ft_itoa)
{
	m_safe_string_assert(ft_itoa(42), "42", true);
	m_safe_string_assert(ft_itoa(-42), "-42", true);
	m_safe_string_assert(ft_itoa(0), "0", true);
	m_safe_string_assert(ft_itoa(-2147483648), "-2147483648", true);
}

